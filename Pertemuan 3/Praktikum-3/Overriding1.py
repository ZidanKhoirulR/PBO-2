def print_sorted(obj):
    sorted_obj = sorted(obj)
    print("Objek yang diurutkan:", sorted_obj)


print_sorted([3, 1, 4, 2, 5])  # output: Objek yang diurutkan: [1, 2, 3, 4, 5]
# output: Objek yang diurutkan: ['apple', 'banana', 'orange']
print_sorted(("orange", "apple", "banana"))
